#include <iostream>

using namespace std;

int main(){
    int a[4][2];
    for(int i = 0; i < 2; i++){
        cin>>a[i][0]>>a[i][1];
    }
    int x = abs(a[0][0]-a[1][0]), y = abs(a[0][1]-a[1][1]);
    if(x == 0){
        a[2][0] = a[0][0] + y;
        a[3][0] = a[0][0] + y;
        a[2][1] = a[0][1];
        a[3][1] = a[1][1];
        cout<<a[2][0]<<" "<<a[2][1]<<" "<<a[3][0]<<" "<<a[3][1];
    }
    else if(y == 0){
        a[2][1] = a[0][1] + x;
        a[3][1] = a[0][1] + x;
        a[2][0] = a[0][0];
        a[3][0] = a[1][0];
        cout<<a[2][0]<<" "<<a[2][1]<<" "<<a[3][0]<<" "<<a[3][1];
    }
    else if(x == y){
        a[2][0] = a[1][0];
        a[2][1] = a[0][1];
        a[3][0] = a[0][0];
        a[3][1] = a[1][1];
        cout<<a[2][0]<<" "<<a[2][1]<<" "<<a[3][0]<<" "<<a[3][1];
    }
    else{
        cout<<-1;
    }
    return 0;
}